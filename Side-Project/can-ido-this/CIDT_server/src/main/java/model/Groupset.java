package model;

public class Groupset
{
    private int shifterId;
    private int derailleurId;
    private int cassetteId;
    private int crankarmId;
    private int chainringId;
    private int rotorId;
    private int brakesetId;

    public int getShifterId()
    {
        return shifterId;
    }

    public int getDerailleurId()
    {
        return derailleurId;
    }

    public int getCassetteId()
    {
        return cassetteId;
    }

    public int getCrankarmId()
    {
        return crankarmId;
    }

    public int getChainringId()
    {
        return chainringId;
    }

    public int getRotorId()
    {
        return rotorId;
    }

    public int getBrakesetId()
    {
        return brakesetId;
    }
}
